    private void downStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downStartActionPerformed
        // TODO add your handling code here:
        downStart.setVisible(false);
        downStop.setVisible(true);
        downLabelStop.setText("");
        downLabelStart.setText("Start");
        downSecond.disable();
        downMinute.disable();
        downHour.disable();
        down = true;
        Thread thread;
        thread = new Thread() {

            @Override
            public void run() {
                int displaySecond;
                int displayMinutes;
                int displayHours;
                boolean x = true;

                if (downSecond.getText().isEmpty()) {
                    displaySecond = 0;
                } else {
                    displaySecond = Integer.parseInt(downSecond.getText());
                }

                if (downMinute.getText().isEmpty()) {
                    displayMinutes = 0;
                } else {
                    displayMinutes = Integer.parseInt(downMinute.getText());
                }

                if (downHour.getText().isEmpty()) {
                    displayHours = 0;
                } else {
                    displayHours = Integer.parseInt(downHour.getText());
                }

                if (displaySecond > 59) {
                    displayMinutes += (displaySecond / 60);
                    displaySecond %= 60;

                }
                if (displayMinutes > 59) {
                    displayHours += (displayMinutes / 60);
                    displayMinutes %= 60;

                }
                if (displaySecond == 0 && displayMinutes == 0 && displayHours == 0) {
                    System.out.println("Times Up!");
                    notify.setText("Times Up!");
                    downSecond.setText("00");
                    downMinute.setText("00");
                    downHour.setText("00");
                    this.stop();
                }

                while (x) {
                    if (down == true) {
                        try {
                            if (displaySecond < 10) {

                            }
                            downSecond.setText(Integer.toString((int) displaySecond));
                            downMinute.setText(Integer.toString((int) displayMinutes));
                            downHour.setText(Integer.toString((int) displayHours));
                            sleep(1000);
                            if (displaySecond > 0) {
                                displaySecond--;
                            } else {
                                displaySecond = 59;
                                displayMinutes--;
                                if (displayMinutes < 0) {
                                    displayMinutes = 59;
                                    displayHours--;
                                }
                            }

                            if (displaySecond == 0 && displayMinutes == 0 && displayHours == 0) {
                                System.out.println("Times Up!");
                                notify.setText("Times Up!");
                                downSecond.setText("00");
                                downMinute.setText("00");
                                downHour.setText("00");
                                this.stop();
                            }

                            downSecond.setText(Integer.toString((int) displaySecond));
                            downMinute.setText(Integer.toString((int) displayMinutes));
                            downHour.setText(Integer.toString((int) displayHours));

                        } catch (InterruptedException ex) {
                            Logger.getLogger(FourNOne_TimeApp.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }

                }
            }

        };
        thread.start();

    }//GEN-LAST:event_downStartActionPerformed

