    private void stopwatchStartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_stopwatchStartMouseClicked
        stopwatchStart.setVisible(false);
        stopwatchStop.setVisible(true);
        stopwatchLabelStop.setText("");
        stopwatchLabelStart.setText("Start");
        watch = true;
        jLabelStopWatch.setText("00:00:00");
        Thread thread;
        thread = new Thread() {

            @Override
            public void run() {

                boolean x = true;
                long displayMinutes = 0;
                long displayHours = 0;
                long starttime = System.currentTimeMillis();
                System.out.println(displayHours + ":" + displayMinutes + ":0");

                while (x) {
                    if (watch == true) {
                        try {
                            sleep(1000);
                            long timepassed = System.currentTimeMillis() - starttime;
                            long secondspassed = timepassed / 1000;
                            if (secondspassed == 60) {
                                displayMinutes++;
                                secondspassed = 0;
                                starttime = System.currentTimeMillis();
                            }
                            if (displayMinutes == 60) {
                                displayMinutes = 0;
                                displayHours++;
                            }
                            if (displayHours == 24) {
                                displayHours = 0;
                                displayMinutes = 0;
                                secondspassed = 0;
                                starttime = System.currentTimeMillis();
                            }
                            jLabelStopWatch.setText(displayHours + ":" + displayMinutes + ":" + secondspassed);
                            System.out.println(displayHours + ":" + displayMinutes + ":" + secondspassed);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(FourNOne_TimeApp.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }

                }
            }

        };
        thread.start();

        // TODO add your handling code here:
    }//GEN-LAST:event_stopwatchStartMouseClicked

